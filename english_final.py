def predict(image_path,text):
    image=Image.open(image_path).convert("RGB")
    enc=processor(
        text=str(text)[:300],
        images=image,
        truncation=True,
        padding="max_length",
        max_length=77,
        return_tensors="pt"
    )
    for k in enc:
        enc[k]=enc[k].to(device)
    with torch.no_grad():
        logits=model(
            enc["input_ids"],
            enc["attention_mask"],
            enc["pixel_values"]
        )
    return inv[int(torch.argmax(logits,1))]

